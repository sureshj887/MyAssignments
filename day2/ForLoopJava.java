package week1.day2;

public class ForLoopJava {

	public static void main(String[] args) {
		int num = 10;
		//For Ascending
		for (int i = 0; i <= num ; i++) {
			System.out.println(i);
			
		}
		
		//For Descending
		for (int i = num; i >=0 ; i--) {
			System.out.println(i);
	
}
	}

}
