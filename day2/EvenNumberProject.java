package week1.day2;

public class EvenNumberProject {

	public static void main(String[] args) {
	int num = 16;
	
	for (int i = 0; i <=num; i++) {
		if(i%2==0) {
			System.out.println("Even numbers is: "+i);
		}
	}
	
	for (int j = 0; j <=num; j++) {
		if(j%2==1) {
			System.out.println("Odd numbers is: "+j);
		}
	}
		
	}
	
	}


